package ch02.sec06;

public class StringExample {

	public static void main(String[] args) {
		String name = "홍길동";
		String job = "프로그래머";
		System.out.println(name);
		System.out.println(job);

		
		String str = "나는 \"자바\"를 배웁니다.";
		System.out.println(str);
		
		str = "나는 \t자바\t를 배웁니다.";
		System.out.println(str);
		
		System.out.println("현재 출력\n\n\n\n\n\n");
		
	}

}
